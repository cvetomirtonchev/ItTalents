
public class ForLoop {

	public static void main(String[] args) {
	
		//while
		int x = 1;
		while(x <= 10){
			System.out.println(x);
			x++;
		}
		
		//do-while
		int y = 1;
		do{
			System.out.println(y);
			y++;
		}while(y<=10);
		
		//for
		for(int z = 1; z <= 10; z++){
			System.out.println(z);
		}
	}
}
